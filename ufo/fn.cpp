std::string init_answer(std::string codeword) {
  std::string answer = ""; // displays correctly guessed letters, first initialized with underscores 
  for (int i = 0; i < codeword.length(); i++) {
    answer += "_"; 
  } 
  return answer; 
}

void end_game(std::string answer, std::string codeword) {
  
  if (answer == codeword) {
    std::cout << "Hooray! You saved the person and earned a medal of honor!\n"; 
  } else {
    std::cout << "Oh no! The UFO just flew away with another person!\n"; 
    std::cout << "The codeword was: " + codeword << std::endl;
  }
}

void display_status(std::string answer, std::vector<char> incorrect) {
    
    std::cout << "Incorrect guesses: \n";
    for (int i = 0; i < incorrect.size(); i++) {
      std::cout << incorrect[i] << " ";
      if (i == incorrect.size() - 1) {
        std::cout << "\n"; 
      } 
    }

    std::cout << "Codeword: \n"; 
    for (int i = 0; i < answer.length(); i++) {
      std::cout << answer[i] << " "; 
      if (i == answer.length() - 1) {
        std::cout << "\n"; 
      }
    }
}

void check_letter(char &letter, std::vector<char> incorrect) {
  if (incorrect.size() > 0) {
    while (std::find(incorrect.begin(), incorrect.end(), letter) != incorrect.end()) {
      std::cout << "Incorrect letter already guessed. Please try another: "; 
      std::cin >> letter; 
      letter = std::tolower(letter); 
    }   
  }
}

void update_answer(char letter, std::string codeword, std::string &answer, bool &guess) {
  // modifies answer and guess in place 
  for (int i = 0; i < codeword.length(); i++) {
      if (letter == codeword[i]) {
        guess = true; 
        answer[i] = letter; // reveal letter 
      }
    } 
}


